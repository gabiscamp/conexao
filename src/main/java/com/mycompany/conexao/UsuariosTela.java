    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
         int linha = tabelaUsuarios.getSelectedRow();
    if (linha != -1) {
        String loginAntigo = tabelaUsuarios.getValueAt(linha, 0).toString();
        
        String novoLogin = JOptionPane.showInputDialog(this, "Novo login:", loginAntigo);
        String novaSenha = JOptionPane.showInputDialog(this, "Nova senha:");
        
        if (novoLogin != null && novaSenha != null && !novoLogin.isEmpty() && !novaSenha.isEmpty()) {
            UsuarioCRUD crud = new UsuarioCRUD();
            if (crud.atualizarUsuario(loginAntigo, novoLogin, novaSenha)) {
                JOptionPane.showMessageDialog(this, "Usu치rio alterado com sucesso!");
                btnAtualizar.doClick();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um usu치rio.");
    }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
    UsuarioCRUD crud = new UsuarioCRUD();
    List<String[]> lista = crud.listarUsuarios();
    
    DefaultTableModel modelo = (DefaultTableModel) tabelaUsuarios.getModel();
    modelo.setRowCount(0); // Limpa a tabela
    
    
    for (String[] u : lista) {
        modelo.addRow(new Object[]{u[0], u[1]});
    }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int linha = tabelaUsuarios.getSelectedRow();
    if (linha != -1) {
        String login = tabelaUsuarios.getValueAt(linha, 0).toString();
        UsuarioCRUD crud = new UsuarioCRUD();
        if (crud.excluirUsuario(login)) {
            JOptionPane.showMessageDialog(this, "Usu치rio deletado!");
            btnAtualizar.doClick(); // Atualiza tabela
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao deletar.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um usu치rio.");
    }
    }//GEN-LAST:event_btnDeletarActionPerformed

